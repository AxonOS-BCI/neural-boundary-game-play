import fs from 'node:fs';
import vm from 'node:vm';
const html = fs.readFileSync('index.html','utf8');
const js = fs.readFileSync('app.js','utf8');
if (!html.includes('id="run"')) throw new Error('missing run button');
if (!html.includes('RUN GAME')) throw new Error('missing RUN GAME copy');
if (!js.includes('function start(')) throw new Error('missing start function');
if (!js.includes('BoundaryRun')) throw new Error('missing BoundaryRun browser hook');
new vm.Script(js, { filename: 'app.js' });
console.log('SMOKE OK: index/app parse and required hooks exist');
